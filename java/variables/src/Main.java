// Define a class named Main
public class Main {
    // Main method
    public static void main(String[] args) {
        // Declare an integer variable and initialize it with 10
        int num = 10;
        // Declare a double variable for pi and assign a value
        double pi = 3.14;
        // Declare a character variable and assign the letter 'A'
        char letter = 'A';
        // Declare a boolean variable and set it to true
        boolean isJavaFun = true;

        // Print the value of the integer variable
        System.out.println("Number: " + num);
        // Print the value of the double variable
        System.out.println("Pi: " + pi);
        // Print the value of the character variable
        System.out.println("Letter: " + letter);
        // Print the value of the boolean variable
        System.out.println("Is Java fun? " + isJavaFun);
    }
}